public class inverted_left_traingle {
    public static void main(String[] args) {
        System.out.println(" inverted left trinagle");
        for(int i=5; i>0; i--) {
        for(int j=0; j<i; j++){
            System.out.print("*");
        }
         System.out.print("\n");
     }   
    }
}
