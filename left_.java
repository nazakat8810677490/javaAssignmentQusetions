public class left_ {
    public static void main(String[] args) {
        System.out.println(" left squar pattern");
      for(int i=0; i<5; i++){
        for(int j=5-i; j>0; j--){
            System.out.print(" ");
            
        }
        for(int k=0; k<5; k++){
            System.out.print("*");
        }
        System.out.print("\n");
        
     }
    }
    
}
