public class right_suare {
    public static void main(String[] args) {
         System.out.println(" right slide squar pattern");
for(int i=0; i<5; i++){
        for(int j=0; j<i; j++){
            System.out.print(" ");
        }
        for(int k=0; k<5; k++){
            System.out.print("*");
        }
        System.out.print("\n");
     }
    }    
}
