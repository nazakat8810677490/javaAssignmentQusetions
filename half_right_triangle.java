public class half_right_triangle {
    public static void main(String[] args) {
        System.out.println("half right triangle");
for(int i=1; i<5; i++){
        for(int j=5-i; j>1; j--){
            System.out.print(" ");
        }
        for(int k=1; k<=i; k++){
            System.out.print("*");
        }
    System.out.println();
     }   

    }
}
